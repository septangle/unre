package com.unre.photo.util;

import java.io.File;

public class ImageInfo {
	
	private File imagePath;
	
	private Long id;

	



	public File getImagePath() {
		return imagePath;
	}

	public void setImagePath(File imagePath) {
		this.imagePath = imagePath;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	
	

}
